"""
Начальник дал Ване задачу возвести в квадрат два числа и сложить полученные результаты. Но Ваня очень разволновался и
допустил несколько ошибок:

sq = landba x, y; return x**3 - y*2

Вам, как опытному коллеге, необходимо исправить ошибки Вани, чтобы начальник не лишил его зарплаты.  Исправления нужно
сделать только в пределах одной строки.

Вводить и выводить ничего не нужно

Sample Input 1:

8
11
Sample Output 1:

185.0
Sample Input 2:

1
2
Sample Output 2:

5.0
Sample Input 3:

3
0.9
Sample Output 3:

9.81
Sample Input 4:

10.0
1.5
Sample Output 4:

102.25
"""


sq = lambda x, y: x**2 + y**2


assert sq(0, 0) == 0        # 0^2 + 0^2 = 0
assert sq(3, 4) == 25       # 3^2 + 4^2 = 9 + 16 = 25
assert sq(1, -1) == 2       # 1^2 + (-1)^2 = 1 + 1 = 2
assert sq(-2, 5) == 29      # (-2)^2 + 5^2 = 4 + 25 = 29
assert sq(10, 0) == 100     # 10^2 + 0^2 = 100
print('Все проверки пройдены!')
